# Lista de datos (arreglo)
cities = ['Medellín', 'Cali', 'Bogotá', 'Santa Marta', 'Cartagena']
print(cities)

# Recorrer la lista en búsqueda de un item del mismo
for city in cities:
    city = 'Medellín'
print(city)

